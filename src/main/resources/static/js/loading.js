function veioDePagina(nomePagina) {
    const referrer = document.referrer.toLowerCase();

    return referrer.includes(nomePagina);
}

function descobrirPaginaAtual() {

    const path = window.location.pathname.toLowerCase();

    if (path.includes("login")) {

        if (veioDePagina("dashboard")) {
            return "login_saindo_dashboard";
        }

        if (veioDePagina("cadastro")) {
            return "cadastro_para_login";
        }

        return "login";
    }

    if (path.includes("cadastro")) {

        if (veioDePagina("login")) {
            return "login_para_cadastro";
        }

        return "cadastro";
    }

    if (path.includes("dashboard")) return "dashboard";

    if (path.includes("desafio")) return "desafios";

    if (path.includes("anuncio-recompensa"))
        return "anuncio-recompensa";

    if (path.includes("inventario"))
        return "inventario";

    return "padrao";
}

function setRandomLoadingText() {

    const textEl = document.getElementById("loading-text");

    if (!textEl) return;

    const pagina = descobrirPaginaAtual();

    const mensagens =
        loadingMessagesPorPagina[pagina]
        || loadingMessagesPorPagina.padrao;

    const message =
        mensagens[Math.floor(Math.random() * mensagens.length)];

    textEl.textContent = message;
}

document.addEventListener("DOMContentLoaded", () => {

    setRandomLoadingText();

    const loading = document.getElementById("loading");

    if (loading) {

        setTimeout(() => {

            loading.style.opacity = "0";

            setTimeout(() => {
                loading.remove();
            }, 300);

        }, 1000);
    }
});

